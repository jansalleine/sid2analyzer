unsigned int SIDANALYZER_PRG_LEN = 1986;

int main(int argc, char *argv[])
{
    char    outfile_name[255]   = "out.prg";

    unsigned int do_compress    = 1;
    unsigned int i              = 0;
    unsigned int j              = 0;
    unsigned int input_byte     = 0;

    char    *infile_name        = NULL;
    char    *infile_nopath      = NULL;
    long int infile_len         = 0;

    FILE    *infile             = NULL;
    FILE    *outfile            = NULL;

    unsigned int music_init_lo  = 0xFF;
    unsigned int music_init_hi  = 0xFF;
    unsigned int music_play_lo  = 0xFF;
    unsigned int music_play_hi  = 0xFF;
    unsigned int music_start    = 0xFFFF;
    unsigned int music_end      = 0xFFFF;
    unsigned int music_end_lo   = 0xFF;
    unsigned int music_end_hi   = 0xFF;
    unsigned int bytes_2_fill   = 0xFF;
    unsigned int outfile_start  = 0x0801;
    unsigned int outfile_end    = 0xFFFF;

    unsigned int sid_speed      = 0xFFFFFFFF;

    if (argc == 1
        || strcmp(argv[argc-1], "-h") == 0
        || strcmp(argv[argc-1], "--help") == 0)
    {
        print_help();
        exit(-1);
    }

    for (i = 1; i < argc - 1; i++)
    {
        if (strcmp(argv[i], "-o") == 0)
        {
            strncpy(outfile_name, argv[++i], sizeof(outfile_name) - 1);
            outfile_name[sizeof(outfile_name) - 1] = '\0';
        }

        if (strcmp(argv[i], "-nc") == 0)
        {
            do_compress = 0;
        }
    }

    if (i >= argc)
    {
        fprintf(stderr, "[ERROR]: No input file given.\n");
        exit(EXIT_FAILURE);
    }

    infile_name = newstr(argv[i]);
    infile_nopath = basename(infile_name);

    printf("input  filename:   %s\n", infile_nopath);
    printf("output filename:   %s\n", outfile_name);

    // create outfile and make sure it's writeable
    outfile = fopen(outfile_name, "w");
    if (outfile == NULL)
    {
        fprintf(stderr, "[ERR]: couldn't create file \"%s\".\n", outfile_name);
        exit(EXIT_FAILURE);
    }

    infile = fopen(infile_name, "r");
    if (infile == NULL)
    {
        fprintf(stderr, "[ERROR]: couldn't open file \"%s\".\n", infile_name);
        exit(EXIT_FAILURE);
    }

    for (i = 0; i < SIDANALYZER_PRG_LEN; i++)
    {
        C64_MEMORY[0x0801 + i] = SIDANALYZER_PRG[i];
    }

    fseek(infile, 0, SEEK_END);
    infile_len = ftell(infile);
    fseek(infile, 0x0A, SEEK_SET);
    music_init_hi = fgetc(infile);
    fseek(infile, 0x0B, SEEK_SET);
    music_init_lo = fgetc(infile);
    fseek(infile, 0x0C, SEEK_SET);
    music_play_hi = fgetc(infile);
    fseek(infile, 0x0D, SEEK_SET);
    music_play_lo = fgetc(infile);
    music_start = 0x0000 | ((music_init_hi << 8) + music_init_lo);

    printf("infile_len: 0x%04lX\n", infile_len);
    printf("music_init_lo: 0x%02X\n", music_init_lo);
    printf("music_init_hi: 0x%02X\n", music_init_hi);
    printf("music_play_lo: 0x%02X\n", music_play_lo);
    printf("music_play_hi: 0x%02X\n", music_play_hi);
    printf("music_start: 0x%04X\n", music_start);

    if (music_start < 0x1000)
    {
        fprintf(stderr, "[ERROR]: music_start below 0x1000: 0x%04X.\n", music_start);
        fclose(infile);
        fclose(outfile);
        exit(EXIT_FAILURE);
    }

    C64_MEMORY[0x085C]     = music_init_lo;
    C64_MEMORY[0x085D]     = music_init_hi;
    C64_MEMORY[0x085F]     = music_init_lo;
    C64_MEMORY[0x0863]     = music_init_hi;
    C64_MEMORY[0x0A4D]     = music_init_lo;
    C64_MEMORY[0x0A4E]     = music_init_hi;
    C64_MEMORY[0x0D6F]     = music_play_lo;
    C64_MEMORY[0x0D70]     = music_play_hi;

    fseek(infile, 0x12, SEEK_SET);

    for (i = 0x18; i > 0; i-=0x08)
    {
        input_byte = fgetc(infile);
        sid_speed = (sid_speed & (input_byte << i));
    }

    printf("sid_speed: 0x%08X\n", sid_speed);

    if (sid_speed > 0)
    {
        fprintf(stderr, "[ERROR]: sid_speed not 0x00000000: 0x%08X.\n", sid_speed);
        fclose(infile);
        fclose(outfile);
        exit(EXIT_FAILURE);
    }

    fseek(infile, 0x7E, SEEK_SET);

    i = 0;
    while ((input_byte = fgetc(infile)) != EOF)
    {
        C64_MEMORY[music_start+i] = input_byte;
        i++;
    }

    fclose(infile);

    music_end = music_start+i;
    music_end_lo = music_end & 0xFF;
    music_end_hi = (music_end & 0xFF00) >> 8;
    printf("music_end: 0x%04X\n", music_end);
    printf("music_end_lo: 0x%02X\n", music_end_lo);
    printf("music_end_hi: 0x%02X\n", music_end_hi);

    C64_MEMORY[0x0A5F]     = music_end_hi + 0x01;
    C64_MEMORY[0x0A63]     = music_init_hi;
    C64_MEMORY[0x0A71]     = music_init_hi - 0x01;
    C64_MEMORY[0x0A75]     = music_end_hi;

    bytes_2_fill = 0x100 - (music_end & 0xFF);

    for (i = 0; i < bytes_2_fill; i++)
    {
        C64_MEMORY[music_end + i] = 0x20;
    }

    outfile_end = music_end + i;

    if (do_compress)
    {
        j = 0;
        for (i = outfile_start; i < outfile_end; i++)
        {
            EXOMIZER_IN[j] = C64_MEMORY[i];
            j++;
        }

        EXOMIZER_IN_LEN = j;
        EXOMIZER_OUT_LEN = exomizer(
            EXOMIZER_IN, EXOMIZER_IN_LEN, 0x0801, 0x0840, EXOMIZER_OUT
        );

        for (i = 0; i < EXOMIZER_OUT_LEN; i++)
        {
            fputc(EXOMIZER_OUT[i], outfile);
        }
    }
    else
    {
        fputc(0x01, outfile);
        fputc(0x08, outfile);

        for (i = outfile_start; i < outfile_end; i++)
        {
            fputc(C64_MEMORY[i], outfile);
        }
    }

    fclose(outfile);

    exit(EXIT_SUCCESS);
}

// string helper functions

char *newstr(char *initial_str)
{
    int num_chars;
    char *new_str;

    num_chars = strlen(initial_str) + 1;
    new_str = malloc (num_chars);

    strcpy (new_str, initial_str);

    return new_str;
}

